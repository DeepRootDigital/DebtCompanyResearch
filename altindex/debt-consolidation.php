				<h4>What exactly is debt consolidation?</h4>
				<p>Debt consolidation is a term that's often thrown around when discussing different debt relief solutions. However, there is often confusion about exactly what it means. Debt consolidation itself is not a debt relief solution. Rather it is a by-product of such debt relief solution like credit counseling and debt settlement. With both types of programs you consolidate your debts into a single monthly payment.
