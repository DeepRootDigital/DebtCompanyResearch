				<h4>How can I consolidate my debts?</h4>
				<p>Typically there are 3 primary ways to consolidate your debts:</p>
				<p>1.	Enter a debt settlement program which is typically an 18-36 month program with a company who helps you negotiate down the amount of debts that you owe. You can read more about debt settlement here.</p>
				<p>2.	Enter a credit counseling program which is typically a 2-5 year program where you pay back everything you owe but on a scheduled monthly plan with lower interest rates and fees. You can read more about credit counseling here.</p>
				<p>3.	Get a consolidation loan. This option is harder to come by as most loans need some collateral such as a house with equity in it. Banks and other lenders will not lend to people unless they have something they can take from you if you are unable to repay the loan.</p>
				<h4>What debts qualify for debt consolidation?</h4>
				<p>In most cases people seek to consolidate their unsecured debts which are debts that are not backed by collateral (a house, a car, etc). The most common unsecured debts are credit cards but also utility bills and medical bills sometimes qualify.</p>
				<h4>What debts do not qualify for debt consolidation?</h4>
				<p>The most common are student loan debts, tax debts, and payday loan debts. There is help available for all three, however they are special situations which are typically handled by specialists. Debt settlement and credit counseling firms are unlikely to be able to assist you with these 3 types of debt.</p>
